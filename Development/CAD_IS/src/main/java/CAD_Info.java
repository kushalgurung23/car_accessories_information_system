    private void openMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openMenuItemActionPerformed
        // TODO add your handling code here:
        //Opens any file from Desktop interface.
        JFileChooser filechooser = new JFileChooser();
        int i = filechooser.showOpenDialog(this);
            if(i == JFileChooser.APPROVE_OPTION) 
            {
                File file = filechooser.getSelectedFile();
                    //Opening file
                    //Checks if desktop interaction is available or not from this platform.
                    if(Desktop.isDesktopSupported()) 
                    {
                        try
                        {      
                        File openFile = new File(file.getAbsolutePath());
                        Desktop.getDesktop().open(openFile);  //opens the absolute form of this abstract pathname.
                        }
                        catch (IOException iOException)
                        {
                            //Showing the path in a dialog box.
                        JOptionPane.showMessageDialog(this, "No application available to open the file you chose.\n"
                                + "Chosen File location: \n" + file.getAbsolutePath(), "Error!", JOptionPane.PLAIN_MESSAGE);
                        }
                        catch(Exception filenfException) 
                        {
                            JOptionPane.showMessageDialog(this, "There was an error while accessing the file.\n", "Error!", JOptionPane.PLAIN_MESSAGE);
                        }
                    } 
                    else
                    {
                         Toolkit.getDefaultToolkit().beep();
                    JOptionPane.showMessageDialog(this, "Desktop interaction was denied.", "Information", JOptionPane.INFORMATION_MESSAGE);
                    }
            }
            else
            { 
                //If user doesn't select files and click exits in the file chooser dialog box.
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "No file was selected.", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
    }//GEN-LAST:event_openMenuItemActionPerformed

