    private void priceSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_priceSearchButtonActionPerformed
        // TODO add your handling code here
        //Search by price:
        try
        {
        int rowCount = jTable1.getRowCount();
        String search = priceSearchTextField.getText();
        if(!"".equals(search) && rowCount!=0) 
        {
            int searchItem = Integer.parseInt(priceSearchTextField.getText());  //The value that we enter in the price search text field.
            int data[] = new int[rowCount];
            for(int i=0; i<rowCount; i++) 
            {
                data [i] = Integer.parseInt(jTable1.getValueAt(i, 5).toString());  //Since our price is on 5th position, data [i] will get the value of 5th position.
            }
            int searchResult = binarySearch(data, searchItem);    //binary search method is called.
            if (searchResult != -1) 
            {
                for (int i = 0; i < rowCount; i++) 
                {
                    if (Integer.parseInt(jTable1.getValueAt(i, 5).toString()) == searchResult) 
                    {
                        int itemIndex = 2;
                        String itemName = jTable1.getValueAt(i, itemIndex).toString();  //Our accessories name is stored at 2nd position, itemIndex value is assigned with 2.
                        JOptionPane.showMessageDialog(this, "Search Found!\nPrice: Rs." + searchItem + "\nAccessories: " + itemName, "Result", JOptionPane.INFORMATION_MESSAGE);
                        break;                  
                    }
                }
            } 
            else 
            {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "No accessories are available for this price.", "Result", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        else 
        {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "Please enter price.", "Error", JOptionPane.WARNING_MESSAGE);
        }
        }
        catch(NumberFormatException exception) 
        {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(this, "Please enter price amount in whole number.","Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_priceSearchButtonActionPerformed

     
    private void categorySearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_categorySearchButtonActionPerformed
        // TODO add your handling code here:
        //search by category.
        String categoryItems = categoryComboBox.getSelectedItem().toString();
        int searchIndex1 = 1;
        int rows1 = jTable1.getRowCount();
        int no = 1;
        String displayName="";
        for (int i = 0; i < rows1; i++) 
        {
            if ((jTable1.getValueAt(i, searchIndex1).toString()).equals(categoryItems))
            { 
            //searchIndex1 is assigned with 1 since our category is at 1st position of the array.
                int itemIndex = 2;
                displayName = displayName+ no + "." + ((String) jTable1.getValueAt(i, itemIndex)) + "\n";
                no++;
            }
        }
        if (!"".equals(displayName))
        {
            JOptionPane.showMessageDialog(this, displayName, (no-1) + " accessories found in " + categoryItems+" category.", JOptionPane.INFORMATION_MESSAGE);
        } 
        else 
        {
            JOptionPane.showMessageDialog(this, "No Items Related to Category: " + categoryItems, "Message", JOptionPane.INFORMATION_MESSAGE);
        }              
    }//GEN-LAST:event_categorySearchButtonActionPerformed

