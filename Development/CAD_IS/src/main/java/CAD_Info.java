    private void accessoriesNameTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_accessoriesNameTextFieldKeyTyped
        // TODO add your handling code here:
        //This method will not allow to enter integer value in accessories name.
        char obj = evt.getKeyChar();
        if(!(Character.isAlphabetic(obj))) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_accessoriesNameTextFieldKeyTyped
    
