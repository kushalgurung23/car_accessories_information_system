      public void sort(int [] a)
      {        
      // This method will sort the value of an array which is passed as an argument in this method's parameter.
	for(int i=0;i<a.length;i++)
        {
	int minVal = minimumValue(a,i);
	swap(a,minVal,i);	
        }
      }

    public int minimumValue(int [] a, int form)
    {
     //This method finds the minimum value by comparing one number with another.
	int minVal = form;
	for(int i = form +1;i<a.length;i++)
        {
            if(a[i]<a[minVal])
            {
		minVal = i;
            }
	}
        return minVal;		
    }
    
    public void swap(int [] a,int i,int j)
    {
    //This method will swap the position of smaller and greater number. It keeps smaller number at first.
	int temp= a[i];
	a[i] = a[j];
	a[j] = temp;
    }
    
    
    public int binarySearch(int [] a, int key) 
    {
    //This is a binary search method which will be called later while searching for accessories through price.
       sort(a);
       int size = a.length;
       int low = 0;
       int high = size-1;
       while (low <= high) 
       {
            int mid = (low + high) / 2;
            if (a[mid] == key) 
            {
                return a[mid];
            }
            else if (key > a[mid]) 
            {
                low = mid + 1;
            } 
            else 
            {
                high = mid - 1;
            }
        }
        return -1;
    }
