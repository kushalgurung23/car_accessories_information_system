    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String range = "";
        String recommendation = "";
        String category = jComboBox1.getSelectedItem().toString();
        String accessoriesName = accessoriesNameTextField.getText();
        String accessoriesNumber = accessoriesNumberTextField.getText();
        String price = priceTextField.getText();
        
        if (lowRadioButton.isSelected()) 
        {
            range = lowRadioButton.getText().toString();
        }
        if (mediumRadioButton.isSelected()) 
        {
            range = mediumRadioButton.getText().toString();
        }
        if (highRadioButton.isSelected()) 
        {
            range = highRadioButton.getText().toString();
        }
        if (companyCheckBox.isSelected()) 
        {
            recommendation = String.valueOf("Company");
        }
        if (communityCheckBox.isSelected()) 
        {
            recommendation = String.valueOf("Community");
        }
        if (noRecommendationCheckBox.isSelected()) 
        {
            recommendation = String.valueOf("No Recommendation");
        }
        if (companyCheckBox.isSelected() && communityCheckBox.isSelected())
        {
            recommendation = String.valueOf("Company, Community");
        }
        try
        {
            if(accessoriesNumber.equals("") && accessoriesName.equals("") && range.equals("") && price.equals("") && recommendation.equals("")) 
            {
                 Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "Please enter all the value.", "Error!", JOptionPane.ERROR_MESSAGE);
            }
            else if(accessoriesNumber.equals("")) 
            {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "Please enter the accessories number.", "Error!", JOptionPane.ERROR_MESSAGE);
            }      
            else if(accessoriesName.equals("")) 
            {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "Please enter the accessories name.", "Error!", JOptionPane.ERROR_MESSAGE);
            }
            else if (category.equals(""))
            {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "Please select a accessories category.", "Error!", JOptionPane.ERROR_MESSAGE);
            }
            else if(range.equals(""))
            {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "Please select a accessories prize range.", "Error!", JOptionPane.ERROR_MESSAGE);
            }
            else if(price.equals("")) 
            {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "Please enter the accessories price.", "Error!", JOptionPane.ERROR_MESSAGE);
            }
            else if(Integer.parseInt(price)<0)
            {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "Please enter the correct accessories price.", "Error!", JOptionPane.ERROR_MESSAGE);
            }
            else if(recommendation.equals("")) 
            {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "Please select the accessories recommendations.", "Error!", JOptionPane.ERROR_MESSAGE);
            }
            else
            {  //else condition will run when all the values are added and selected in text fields, combox boxes and radio buttons.
                int id = idNumberArrayList.size()+1;   //It stores the id number which will be added by 1, every time a new accessory is added.
                String idNumber = String.valueOf(id);
                String [] arrayAdd = {idNumber, category, accessoriesName, accessoriesNumber, range, price, recommendation};
                model.addRow(arrayAdd);
                idNumberArrayList.add(arrayAdd);
                JOptionPane.showMessageDialog(this, "Accessory is successfully added.", "Result", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        catch(NumberFormatException exceptionVariable) {
            Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(this, "Please enter correct value in price", "Error!", JOptionPane.ERROR_MESSAGE);
        }  
    }//GEN-LAST:event_addButtonActionPerformed

